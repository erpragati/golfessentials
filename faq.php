     <ul>
          <li class="answer"><b>Is there a charge for registering as a customer?</b></li>
          <span class="answer">Registration is free of charge. There are no hidden fees.</span>

          <li class="answer"><b>Why do I need to register?</b></li>
          <span class="answer">By registering, you will have access to the many facilities available, such as My Account, Wishlist, Special Order and many more.</span>

          <li class="answer"><b>How do I register at golfessentials.in?</b></li>
          <span class="answer">Click on the 'Create an Account' button located at the top of the page. Alternatively, you may click <a href="register">here to register.</a> Fill out the registration form completely and click on the submit button when you're done. It takes less than 2 minutes and you can start using your account immediately after registration.</span>

