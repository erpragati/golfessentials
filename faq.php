          <span class="answer">Your wishlist lists the items that you have saved and would like to purchase at a later date. Items in your wishlist can be purchased at any time or removed from your wishlist in case you no longer desire to purchase them. Please note that you need to sign in to your account in order to use the wishlist option.</span>
     </ul>

